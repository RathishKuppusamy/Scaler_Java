public class DataTypes {
        public static void main(String[] args) {
//  primitive dataypes
//    Numeric
//      Integer
            byte b = 1;
            short s = 12;
            int i = 1000;
            long l = 100000000L;
//      Floating point
            float f = 5.5f;
            double d = 10.5;
            System.out.println(b + " " + s + " " + i + "n" + l + "" + f + " " + d);

//      Non-Numeric
           char c = 'a';
           char c2 = 'b';
           char _1 = '1';
           boolean bool = true;
           String str = "Hello World";

            System.out.println(c + " " + c2 + " " + _1 + " " + bool);
            System.out.println(str);
        }
    }
